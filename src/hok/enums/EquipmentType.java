package hok.enums;

/**
 * Categories of equipment items.
 * Determines which stats the equipment primarily boosts.
 */
public enum EquipmentType {
    ATTACK,     // Boosts physical attack
    DEFENSE,    // Boosts physical defense
    MAGIC,      // Boosts magic power
    MOVEMENT,   // Boosts movement speed
    JUNGLE      // Jungle-specific bonuses
}
