package hok.enums;

/**
 * The six hero classes in Honor of Kings.
 * Each type suggests a different stat distribution.
 */
public enum HeroType {
    TANK,       // High HP, high defense
    WARRIOR,    // Balanced attack and defense
    ASSASSIN,   // High attack, high speed, low HP
    MAGE,       // High magic attack, low defense
    MARKSMAN,   // High physical attack, moderate speed
    SUPPORT     // Moderate stats, team utility
}
